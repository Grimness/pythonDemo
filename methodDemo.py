print(abs(-123)) #abs(-123) = 123 转换正数函数
print(max(1,23,4)) #max(1,23) = 23 获取最大值函数
print(int('123')) #int('123') = 123 转换成整数类型函数
print(float('12.34')) #float('12.34') = 12.34 转换成浮点数类型函数
print(str(123)) #str(123) = '123' 转换成字符串类型函数
print(bool(1)) #bool(1) = True 转换成布尔类型函数 有值为True  无值为False
print(hex(255)) #hex(255) = 0xff 十进制转换十六进制函数
